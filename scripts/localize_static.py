def localize(text: str, locale: str, page: str) -> str:
    # 1. stash the switcher so translation / link-rewriting never touch it
    m = SWITCHER_RE.search(text)
    if not m:
        raise SystemExit(f"no lang-switcher found in {page}")
    suffix = SUFFIX[page]
    text = text[:m.start()] + PLACEHOLDER + text[m.end():]

    if locale != "en":
        # Page-specific strings first, then the shared chrome. The per-page
        # entries are longer, more-specific sentences that often *contain* a
        # chrome phrase (e.g. "Researcher Portal", "Privacy Policy"); running the
        # short chrome replacements first would clobber the substring and leave
        # the surrounding sentence untranslated.
        for old, new in PAGES[locale].get(page, []):
            text = text.replace(old, new)
        for old, new in COMMON[locale]:
            text = text.replace(old, new)
        text = prefix_links(text, locale)
        text = text.replace('<html lang="en">', f'<html lang="{locale}">')

    # 2. rebuild the switcher for this locale and drop it back in
    text = text.replace(PLACEHOLDER, build_switcher(locale, suffix))
    return text


def main(argv: list[str]) -> int:
    targets = argv[1:] or ["es", "fr", "de"]
    # Always re-emit English in place so the in-site switcher stays in sync.
    locales = ["en"] + [t for t in targets if t != "en"]
    for locale in locales:
        for page in PAGES_FILES:
            src = os.path.join(STATIC, page)
            with open(src, encoding="utf-8") as f:
                text = f.read()
            out = localize(text, locale, page)
            if locale == "en":
                dest = src
            else:
                dest_dir = os.path.join(STATIC, locale)
                os.makedirs(dest_dir, exist_ok=True)
                dest = os.path.join(dest_dir, page)
            with open(dest, "w", encoding="utf-8") as f:
                f.write(out)
            print(f"wrote {os.path.relpath(dest, ROOT)}")
    return 0


