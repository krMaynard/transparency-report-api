"""Generate the localized static pages (es / fr / de) from the English originals.

The five static pages (``home.html``, ``index.html``, ``removals.html``,
``portal.html``, ``privacy.html``) are English source-of-truth. This script
produces parallel translations under ``static/<locale>/`` and keeps the
in-site language switcher consistent across every page and locale.

For each target page it:

* removes the ``<details class="lang-switcher">`` block (stashing a placeholder)
  so translation/link-rewriting never touch it,
* translates the shared chrome (``COMMON``) and the per-page strings (``PAGES``),
* rewrites internal links (``/reports`` → ``/es/reports`` …) — the JSON API
  (``/api/*``), Swagger (``/docs``) and external links are left untouched,
* sets ``<html lang="…">`` and localizes a few in-script formatting helpers,
* rebuilds the language switcher for that locale and writes the file out.

English is also re-emitted in place (locale ``en``) so the switcher swap from the
old cross-site globe to the in-site one stays a one-liner here. Re-running is
idempotent. Run from the repo root: ``python scripts/localize_static.py``.
"""
